// 第四部分：println! —— 让程序"说话"
// ============================================================================
//
// 【类比时间】
//
// println! 就像一个"小喇叭"：
//   - 你告诉它说什么，它就大声说出来（显示在屏幕上）
//   - 末尾的 ! 号表示它是一个"宏"（macro），不是普通函数
//   - 现在你只需要记住：println! 就是"打印一行文字"的意思
//
// 【为什么有个感叹号 ! ？】
//
// 在 Rust 里，名字后面带 ! 的叫"宏"（macro）。
// 宏和函数的区别，就像"自动炒菜机"和"手动炒菜"：
//   - 函数（手动）：你放什么进去，它按固定方式处理
//   - 宏（自动）：它可以做一些更灵活、更强大的事情
//
// 现在你不需要深入理解宏，只需要记住：
//   打印文字用 println!（带感叹号）
//

fn first_rust_program() {
    println!("============================================");
    println!("      欢迎来到 Rust 的世界！这是你的第一个程序！      ");
    println!("============================================");

    // 最简单的用法：直接打印一段文字
    // 注意：文字要用双引号 " " 包起来
    println!("你好，世界！");  // 经典的 Hello World

    // 打印空行
    println!("");

    // 用 \n 可以换行（\n 的 n 代表 newline，就是"新行"的意思）
    println!("第一行\n第二行\n第三行");

    // 用 \t 可以加一个制表符（Tab 键的效果，用来对齐）
    println!("姓名：\t张三");
    println!("年龄：\t25");
    println!("城市：\t北京");
}

// ============================================================================
// 第五部分：格式化输出 —— 用 {} 做"占位符"
// ============================================================================
//
// 【类比时间】
//
// 格式化输出就像填空题：
//   - 你先写好句子模板，留几个空（用 {} 表示）
//   - 然后告诉程序把什么值填进去
//
// 例如：
//   模板：  "我叫{}，今年{}岁"
//   填入：  "张三" 和 25
//   结果：  "我叫张三，今年25岁"
//
// 这样你就可以把"固定文字"和"变化的值"混在一起输出了！
//

fn format_output_demo() {
    println!("\n--- 格式化输出演示 ---\n");

    // ---- 基本占位符 {} ----
    // {} 就是一个"坑位"，程序会按顺序把值填进去
    let 名字 = "G-one";  // 用 let 声明一个变量（下节课会详细讲）
    let 年龄 = 20;

    println!("大家好，我叫{}，今年{}岁。", 名字, 年龄);
    //                        ^           ^
    //                        |           |
    //                   第一个坑    第二个坑
    //                   填 "G-one"   填 20

    // ---- 多个占位符 ----
    println!("{}喜欢{}，{}也喜欢{}。", "小明", "苹果", "小红", "香蕉");
    // 输出：小明喜欢苹果，小红也喜欢香蕉。

    // ---- 数字格式化：{:.2} 保留小数位 ----
    // 生活中我们说"圆周率约等于 3.14"，就是保留了 2 位小数
    let 圆周率 = 3.14159265;
    println!("圆周率（保留2位小数）：{:.2}", 圆周率);   // 输出：3.14
    println!("圆周率（保留4位小数）：{:.4}", 圆周率);   // 输出：3.1416

    // 价格显示也经常用到：一件商品 19.9 元，打 8.5 折
    let 原价 = 19.9;
    let 折扣 = 0.85;
    let 折后价 = 原价 * 折扣;
    println!("原价：{:.1}元，折扣：{:.0}%，折后价：{:.2}元", 原价, 折扣 * 100.0, 折后价);
    // 输出：原价：19.9元，折扣：85%，折后价：16.92元
}

// ============================================================================
// 第六部分：注释 —— 给代码写"便签"
// ============================================================================
//
// 【类比时间】
//
// 注释就像贴在冰箱上的便签：
//   - 便签是写给自己（或别人）看的提醒
//   - 电脑不会读便签（注释），它只关心冰箱里的东西（代码）
//
// 注释的作用：
//   1. 解释这段代码在做什么（就像你给自己留的备忘录）
//   2. 临时"关闭"某段代码（让它不执行，但保留在文件里）
//   3. 写文档（/// 文档注释，可以自动生成说明书）
//
// Rust 有三种注释：
//   //    单行注释（最常用）
//   /* */ 多行注释（一次注释很多行）
//   ///   文档注释（用来生成官方文档）
//

fn comment_demo() {
    println!("\n--- 注释演示 ---\n");

    // 这是单行注释：以 // 开头，这一整行都不会被执行
    // 你可以在这里写任何说明文字
    println!("这行代码会执行"); // 行末注释：写在代码后面的说明

    /*
     * 这是多行注释：
     * 以 /* 开头，以 */ 结尾
     * 中间的所有内容都是注释
     * 适合写比较长的说明
     *
     * 注意：多行注释可以嵌套（Rust 的特色功能）
     * 大多数编程语言不支持嵌套多行注释
     */
    println!("多行注释外面的代码会执行");

    // 实际开发中，注释的最佳实践：
    // 1. 不要写"废话注释"——不要给显而易见的代码加注释
    //    坏例子：let x = 5;  // 把 5 赋值给 x（这谁都能看出来！）
    //    好例子：let 速度 = 5;  // 初始速度，单位：米/秒
    //
    // 2. 注释要解释"为什么"，而不是"是什么"
    //    坏例子：// 打印名字
    //            println!("{}", name);
    //    好例子：// 使用全名格式，因为系统要求显示完整姓名
    //            println!("{}", full_name);
    //
    // 3. 保持注释和代码同步更新
    //    如果代码改了，注释也要跟着改，否则会误导别人

    println!("注释是程序员的好朋友，善用它！");
}

