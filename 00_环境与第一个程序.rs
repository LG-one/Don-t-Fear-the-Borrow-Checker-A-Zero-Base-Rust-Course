#[allow(dead_code)]
fn exercise2_multiplication_table() {
    println!("\n--- 练习2：九九乘法表 ---\n");

    // 外层循环：控制行数（第 1 行到第 9 行）
    // for 变量 in 起始..=结束  -- 这是 Rust 的 for 循环语法
    // 1..=9 表示 1, 2, 3, 4, 5, 6, 7, 8, 9（包含 9）
    for i in 1..=9 {
        // 内层循环：每行打印的公式数量
        // 第 1 行打印 1 个，第 2 行打印 2 个，... 第 9 行打印 9 个
        // 所以 j 的范围是 1 到 i
        for j in 1..=i {
            // print! 不会换行，所以同一行的内容会连在一起
            print!("{}×{}={} ", j, i, i * j);
        }
        // 一行的公式都打印完了，换行
        println!("");
    }
}

// ---- 练习 3：字符图案 ----
#[allow(dead_code)]
fn exercise3_char_patterns() {
    println!("\n--- 练习3：字符图案 ---\n");

    let 高度 = 5;  // 三角形的高度

    // ---- 打印等腰三角形 ----
    println!("等腰三角形（高度={}）：", 高度);
    for i in 1..=高度 {
        // 第 i 行需要：
        //   空格数 = 高度 - i（越往下空格越少）
        //   星号数 = 2 * i - 1（越往下星号越多）
        let 空格数 = 高度 - i;
        let 星号数 = 2 * i - 1;

        // " ".repeat(n) 生成 n 个空格
        // "*".repeat(n) 生成 n 个星号
        // println! 里也可以用 {} 占位，填入重复的字符串
        println!("{}{}", " ".repeat(空格数), "*".repeat(星号数));
    }

    println!("");

    // ---- 打印菱形 ----
    println!("菱形（高度={}）：", 高度);

    // 上半部分（包括中间最宽的一行）
    for i in 1..=高度 {
        let 空格数 = 高度 - i;
        let 星号数 = 2 * i - 1;
        println!("{}{}", " ".repeat(空格数), "*".repeat(星号数));
    }

    // 下半部分（倒过来，从第 高度-1 行开始往下减少）
    for i in (1..高度).rev() {
        // .rev() 是"反转"的意思
        // 1..高度 本来是 1, 2, 3, 4
        // .rev() 变成 4, 3, 2, 1
        let 空格数 = 高度 - i;
        let 星号数 = 2 * i - 1;
        println!("{}{}", " ".repeat(空格数), "*".repeat(星号数));
    }

    println!("");

    // ---- 打印空心矩形 ----
    let 宽 = 10;
    let 高 = 5;
    println!("空心矩形（{}×{}）：", 宽, 高);
    for i in 0..高 {
        for j in 0..宽 {
            // 只有在边框位置才打印 *
            // 边框位置：第一行、最后一行、第一列、最后一列
            if i == 0 || i == 高 - 1 || j == 0 || j == 宽 - 1 {
                print!("* ");
            } else {
                print!("  ");  // 内部打印空格
            }
        }
        println!("");
    }
}

