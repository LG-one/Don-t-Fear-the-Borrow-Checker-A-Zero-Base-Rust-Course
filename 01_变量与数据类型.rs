// ---- 练习 1（基础巩固，5-10分钟）：变量遮蔽链 ----
//
// 题目：
// 解释下面每一步发生了什么，说出每行 x 的类型和值
//
fn exercise_1() {
    println!("\n========== 练习 1：变量遮蔽链 ==========");

    // 第 1 步：let x = "42";
    // - 创建一个变量 x
    // - x 的类型是 &str（字符串切片）
    // - x 的值是 "42"
    // - 这是一个字符串，不是数字！
    let x = "42";
    println!("第 1 步: x = \"{}\", 类型: &str", x);

    // 第 2 步：let x: i32 = x.parse().unwrap();
    // - x.parse() 把字符串 "42" 解析成数字
    // - : i32 告诉编译器我们想要 32 位有符号整数
    // - .unwrap() 表示"我确定解析会成功，直接给我结果"
    // - let x 重新声明了 x（遮蔽），新 x 的类型是 i32
    // - 旧的 x（字符串 "42"）被遮蔽了，不再可见
    let x: i32 = x.parse().unwrap();
    println!("第 2 步: x = {}, 类型: i32", x);

    // 第 3 步：let x = x as f64;
    // - x as f64 把 i32 的 42 转换成 f64 的 42.0
    // - let x 再次遮蔽，新 x 的类型是 f64
    // - 旧的 x（i32 的 42）被遮蔽了
    let x = x as f64;
    println!("第 3 步: x = {}, 类型: f64", x);

    // 总结：
    // 整个过程就是：字符串 -> 整数 -> 浮点数
    // 每一步都用 let x 重新声明，所以类型可以改变
    // 如果用 mut 就不行，因为 mut 不能改变变量类型
    println!("\n总结：经过三次遮蔽，x 从字符串 \"42\" 变成了浮点数 42.0");
}

// ---- 练习 2（应用练习，15-20分钟）：元组与数组 ----
//
// 题目：
// 1. 创建一个元组表示学生信息 (name, age, score)
// 2. 用解构提取各个字段
// 3. 创建一个成绩数组，计算最大值、最小值、平均值
//
fn exercise_2() {
    println!("\n========== 练习 2：元组与数组 ==========");

    // Part 1: 学生信息元组
    let student: (&str, u32, f64) = ("G-one", 20, 92.5);

    // 解构
    let (name, age, score) = student;
    println!("学生信息:");
    println!("  姓名: {}", name);
    println!("  年龄: {}", age);
    println!("  成绩: {:.1}", score); // 保留 1 位小数

    // 根据成绩判断等级
    let grade = if score >= 90.0 {
        "优秀"
    } else if score >= 80.0 {
        "良好"
    } else if score >= 60.0 {
        "及格"
    } else {
        "不及格"
    };
    println!("  等级: {}", grade);

    // Part 2: 成绩数组统计
    let scores: [f64; 8] = [85.5, 92.0, 78.5, 96.0, 88.5, 73.0, 91.5, 82.0];
    println!("\n成绩数组: {:?}", scores);

    // 计算最大值
    let mut max = scores[0];
    for &score in scores.iter() {
        if score > max {
            max = score;
        }
    }
    println!("最高分: {:.1}", max);

    // 计算最小值
    let mut min = scores[0];
    for &score in scores.iter() {
        if score < min {
            min = score;
        }
    }
    println!("最低分: {:.1}", min);

    // 计算平均值
    let mut sum = 0.0;
    for &score in scores.iter() {
        sum += score;
    }
    let avg = sum / scores.len() as f64;
    println!("平均分: {:.2}", avg);

    // 统计各等级人数
    let mut excellent = 0; // 优秀 (>=90)
    let mut good = 0;      // 良好 (80-89)
    let mut pass = 0;      // 及格 (60-79)
    let mut fail = 0;      // 不及格 (<60)

    for &score in scores.iter() {
        if score >= 90.0 {
            excellent += 1;
        } else if score >= 80.0 {
            good += 1;
        } else if score >= 60.0 {
            pass += 1;
        } else {
            fail += 1;
        }
    }

    println!("\n等级统计:");
    println!("  优秀: {} 人", excellent);
    println!("  良好: {} 人", good);
    println!("  及格: {} 人", pass);
    println!("  不及格: {} 人", fail);
}

// ---- 练习 3（进阶挑战，选做）：单位转换器 ----
//
// 题目：
// 实现一个简单的单位转换器
//   - 公里 <-> 英里
//   - 公斤 <-> 磅
//
fn exercise_3() {
    println!("\n========== 练习 3：单位转换器 ==========");

    // 转换常量
    const KM_TO_MILES: f64 = 0.621371;  // 1 公里 = 0.621371 英里
    const MILES_TO_KM: f64 = 1.609344;  // 1 英里 = 1.609344 公里
    const KG_TO_POUNDS: f64 = 2.204623; // 1 公斤 = 2.204623 磅
    const POUNDS_TO_KG: f64 = 0.453592; // 1 磅 = 0.453592 公斤

    // 公里 <-> 英里
    println!("\n--- 公里 <-> 英里 ---");
    let distances_km = [1.0, 5.0, 10.0, 42.195, 100.0]; // 马拉松距离也包含在内
    println!("{:<12} {:<12}", "公里 (km)", "英里 (mi)");
    println!("{}", "-".repeat(26));

    for &km in distances_km.iter() {
        let miles = km * KM_TO_MILES;
        println!("{:<12.2} {:<12.2}", km, miles);
    }

    // 反向转换
    println!("\n{:<12} {:<12}", "英里 (mi)", "公里 (km)");
    println!("{}", "-".repeat(26));
    let distances_miles = [1.0, 3.107, 6.214, 26.2, 62.14];
    for &miles in distances_miles.iter() {
        let km = miles * MILES_TO_KM;
        println!("{:<12.2} {:<12.2}", miles, km);
    }

    // 公斤 <-> 磅
    println!("\n--- 公斤 <-> 磅 ---");
    let weights_kg = [1.0, 5.0, 10.0, 50.0, 100.0];
    println!("{:<12} {:<12}", "公斤 (kg)", "磅 (lb)");
    println!("{}", "-".repeat(26));

    for &kg in weights_kg.iter() {
        let pounds = kg * KG_TO_POUNDS;
        println!("{:<12.2} {:<12.2}", kg, pounds);
    }

    // 反向转换
    println!("\n{:<12} {:<12}", "磅 (lb)", "公斤 (kg)");
    println!("{}", "-".repeat(26));
    let weights_pounds = [1.0, 2.2, 11.0, 110.23, 220.46];
    for &pounds in weights_pounds.iter() {
        let kg = pounds * POUNDS_TO_KG;
        println!("{:<12.2} {:<12.2}", pounds, kg);
    }

    // 实用场景
    println!("\n--- 实用场景 ---");

    // 场景1：跑步距离
    let run_km = 5.0;
    let run_miles = run_km * KM_TO_MILES;
    println!("今天跑了 {:.1} 公里 = {:.2} 英里", run_km, run_miles);

    // 场景2：体重
    let weight_kg = 70.0;
    let weight_pounds = weight_kg * KG_TO_POUNDS;
    println!("体重 {:.1} 公斤 = {:.1} 磅", weight_kg, weight_pounds);

    // 场景3：限速转换
    let speed_limit_kmh = 120.0;
    let speed_limit_mph = speed_limit_kmh * KM_TO_MILES;
    println!("限速 {} km/h = {:.1} mph", speed_limit_kmh, speed_limit_mph);
}

