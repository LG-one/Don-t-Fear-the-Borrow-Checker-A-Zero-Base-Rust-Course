    //   → 不需要手动标注！只有一个引用参数，符合省略规则 2
    //   → 编译器自动推断：fn 获取切片<'a>(s: &'a str, ...) -> &'a str
    //
    // fn 首字母大写(s: &str) -> &str
    //   → 不需要手动标注！只有一个引用参数，符合省略规则 2

    // 用标注后的函数测试
    let a = 10;
    let b = 20;
    println!("{} 和 {} 中较大的是：{}", a, b, 返回较大的(&a, &b));

    let s = String::from("Hello, Rust!");
    println!("切片 [0..5]：{}", 获取切片(&s, 0, 5));

    let word = "hello";
    println!("首字母大写：{}", 首字母大写(word));
