// 第二部分：为什么需要生命周期？—— 编译器的困惑
// ============================================================================
//
// 【类比时间】
//
// 想象你是一个图书管理员（编译器）：
//   - 有人来借书，你要记录"这本书什么时候还"
//   - 如果有人说"我要借一本书，但不知道借哪本"
//   - 你会说："你得告诉我是哪本，我才能帮你办借书手续！"
//
// 编译器也一样：
//   - 函数返回引用时，编译器要知道"这个引用来自哪里"
//   - 如果不知道，编译器就无法保证引用是安全的
//   - 所以它会要求你"标注生命周期"
//

fn 为什么需要生命周期() {
    println!("==================================================");
    println!("  第一节：为什么需要生命周期？");
    println!("==================================================\n");

    // ---- 一个可以正常工作的例子 ----
    // 这个函数只有一个参数，返回的引用一定来自这个参数
    // 编译器能自动推断，不需要我们标注
    let s = String::from("你好，世界！");
    let 结果 = 获取第一个字(&s);
    println!("获取第一个字：{}", 结果);

    // ---- 如果有两个参数呢？ ----
    // 编译器不知道返回值来自 x 还是 y
    // 这时候就需要我们手动标注！
    let 字符串1 = String::from("长字符串很长");
    let 字符串2 = String::from("短");
    let 较长的 = longest(字符串1.as_str(), 字符串2.as_str());
    println!("较长的字符串是：{}", 较长的);

    // ---- 为什么需要标注？ ----
    // 如果没有标注，编译器会报错：
    //   error[E0106]: missing lifetime specifier
    //   --> 返回值的生命周期不明确
    //
    // 编译器在说：
    //   "你返回了一个引用，但我不知道它来自 x 还是 y"
    //   "我不知道它能活多久，所以我没法保证它安全"
    //   "请你告诉我！"

    println!();
}

// ---- 辅助函数：获取字符串的第一个字 ----
// 这个函数只有一个引用参数，返回的引用一定来自它
// 编译器能自动推断生命周期（省略规则，后面会讲）
fn 获取第一个字(s: &str) -> &str {
    // as_bytes() 把字符串转成字节数组
    // .iter().enumerate() 遍历每个字节及其索引
    for (i, byte) in s.as_bytes().iter().enumerate() {
        // 中文 UTF-8 编码占 3 个字节，第一个字节 >= 0xE0
        // 英文字母占 1 个字节
        if *byte >= 0xE0 {
            // 找到中文字符的开头，取前 3 个字节
            return &s[..i + 3];
        }
        // 如果是英文，取第一个字符
        if i == 0 {
            return &s[..1];
        }
    }
    s  // 如果没找到，返回整个字符串
}

// ============================================================================
// 第三部分：生命周期标注语法 —— 'a 是什么意思？
// ============================================================================
//
// 【类比时间：借书期限标签】
//
// 想象图书馆给每本书贴了一个标签：
//   - 标签上写着"借阅期限：'a"
//   - 两本书如果贴着同一个标签 'a，说明它们"同一天到期"
//   - 这样图书馆就知道：在 'a 到期之前，这些书都是安全的
//
// 在 Rust 里：
//   - 'a 就是一个"生命周期标签"
//   - 它告诉编译器："这几个引用活一样久"
//   - ' 读作 "tick" 或 "撇"，a 是标签的名字（可以是 'b, 'c 等）
//
// 【语法格式】
//
//   &'a i32       // 一个引用，生命周期是 'a
//   &'a mut i32   // 一个可变引用，生命周期是 'a
//   &'a str       // 一个字符串切片引用，生命周期是 'a
//
// 生命周期标注放在 & 后面、类型前面。
// 它不改变引用活多久，只是告诉编译器"这些引用之间的关系"。
//
// 【重要理解】
//
// 生命周期标注不是在"设置"引用能活多久，
// 而是在"告诉"编译器几个引用之间的关系。
// 就像借书期限标签不是在"规定"书什么时候还，
// 而是在"说明"这几本书是同一批借的、同一天到期。
//

fn 生命周期标注语法() {
    println!("==================================================");
    println!("  第二节：生命周期标注语法");
    println!("==================================================\n");

    // ---- 生命周期标注长什么样？ ----
    // &i32        // 普通引用（编译器自动推断生命周期）
    // &'a i32    // 带生命周期标注的引用
    // &'a mut i32 // 带生命周期标注的可变引用

    // ---- 一个简单的例子 ----
    let x = 42;           // x 的生命周期开始
    let r = &x;           // r 引用 x，r 的生命周期不能超过 x
    println!("r = {}", r); // r 在这里使用，没问题

    // ---- 生命周期标注不会改变程序行为 ----
    // 它只是给编译器的"提示"，帮助编译器理解引用之间的关系
    // 就像给书贴标签不会改变书的内容，只是方便管理

    // ---- 生命周期参数要声明在 <> 里 ----
    // fn foo<'a>(x: &'a str) -> &'a str { ... }
    //       ^^^^ 这里声明了生命周期参数 'a
    // 就像函数的"类型参数"一样，生命周期也是一种"参数"

    println!("  生命周期标注语法总结：");
    println!("  - 'a 是一个生命周期标签（名字随便取）");
    println!("  - 放在 & 后面、类型前面");
    println!("  - 不改变程序行为，只是给编译器的提示");
    println!("  - 声明在函数名后面的 <> 里：fn foo<'a>(...)\n");
}

// ============================================================================
// 第四部分：函数中的生命周期 —— 告诉编译器"谁和谁一样久"
// ============================================================================
//
// 【类比时间：借书期限的承诺】
//
// 你去图书馆借两本书，管理员问：
//   "你这两本书要借到什么时候？"
//   你说："它们是同一批借的，同一天还。"
//   管理员就给两本书贴上同一个期限标签 'a。
//
// 在 Rust 里，函数签名中的生命周期标注就是这个"承诺"：
//   fn longest<'a>(x: &'a str, y: &'a str) -> &'a str
//   这句话告诉编译器：
//   "x 和 y 的生命周期都是 'a"
//   "返回值的生命周期也是 'a"
//   "返回值的生命周期 = x 和 y 中较短的那个"
//
// 【为什么要"较短的那个"？】
//
// 想象你借了两本书：
//   - 书 A 你要借到 12 月 31 日
//   - 书 B 你要借到 6 月 30 日
//   - 你说"它们同一天还"——那实际到期日是哪天？
//   - 答案是 6 月 30 日（较短的那个！）
//
// 因为你必须在两本书都有效的期间内归还，
// 所以实际期限 = 两者中较短的那个。
//
// 返回的引用也一样：它必须在 x 和 y 都有效时才安全，
// 所以它的生命周期 = 两者中较短的那个。
//

fn 函数中的生命周期() {
    println!("==================================================");
    println!("  第三节：函数中的生命周期标注");
    println!("==================================================\n");

    // ---- 正常使用 ----
    let 字符串1 = String::from("这是一个很长很长的字符串");
    let 字符串2 = String::from("短");
    let 结果 = longest(字符串1.as_str(), 字符串2.as_str());
    println!("较长的字符串：{}", 结果);

    // ---- 生命周期在发挥作用 ----
    let 结果;
    {
        let 字符串2 = String::from("临时字符串");
        // 字符串1 和 字符串2 都传入 longest
        // 'a = 两者中较短的生命周期 = 字符串2 的生命周期
        结果 = longest(字符串1.as_str(), 字符串2.as_str());
        println!("在内部作用域使用：{}", 结果);
    } // 字符串2 在这里被释放
    // 如果把 println 移到这个花括号外面，编译器会报错！
    // 因为 结果 的生命周期 = 字符串2 的生命周期
    // 字符串2 已经死了，结果 也就不安全了

    // ---- 看看错误的情况（注释掉的代码会报错） ----
    // let 结果;
    // {
    //     let 字符串2 = String::from("临时字符串");
    //     结果 = longest(字符串1.as_str(), 字符串2.as_str());
    // } // 字符串2 在这里被释放
    // println!("{}", 结果);  // 错误！结果引用了已释放的字符串2

    // ---- 拆解函数签名 ----
    // fn longest<'a>(x: &'a str, y: &'a str) -> &'a str
    //            ^^   ^^^^^^^^   ^^^^^^^^      ^^^^^^^
    //            |       |           |            |
    //       声明生命周期参数   x 的生命周期是 'a   y 的生命周期是 'a   返回值的生命周期也是 'a
    //
    // 整句话的意思：
    //   "x 和 y 的生命周期都叫 'a（它们可能不一样长，但'a取较短的）"
    //   "返回值的生命周期也是 'a（所以返回值不会比 x 或 y 活得更久）"

    println!();
}

// ---- 带生命周期标注的函数 ----
// <'a> 声明了一个生命周期参数 'a
// x: &'a str 表示 x 的生命周期是 'a
// y: &'a str 表示 y 的生命周期也是 'a
// -> &'a str 表示返回值的生命周期也是 'a
// 含义：返回值的生命周期 = x 和 y 中较短的那个
fn longest<'a>(x: &'a str, y: &'a str) -> &'a str {
    if x.len() > y.len() {
        x  // 返回 x
    } else {
        y  // 返回 y
    }
}

// ============================================================================
// 第五部分：结构体中的生命周期 —— 让结构体"借用"数据
// ============================================================================
//
// 【类比时间：临时保管箱】
//
// 想象你有一个保管箱（结构体），里面放着别人借给你的东西（引用）：
//   - 保管箱本身不拥有这些东西
//   - 它只是"借用"了别人的物品
//   - 所以保管箱的寿命不能超过物品主人的寿命
//
// 在 Rust 里，如果结构体里有引用，就必须标注生命周期：
//   struct Excerpt<'a> { text: &'a str }
//   这句话告诉编译器：
//   "Excerpt 结构体借用了 text 这个字符串"
//   "Excerpt 的实例不能比 text 活得更久"
//
// 【为什么要标注？】
//
// 如果不标注，编译器不知道结构体里的引用能活多久，
// 就无法保证结构体被使用时，里面的引用还是有效的。
//
// 就像保管箱管理员不知道你借的东西什么时候要还，
// 他就没法保证你打开保管箱时东西还在。
//

fn 结构体中的生命周期() {
    println!("==================================================");
    println!("  第四节：结构体中的生命周期标注");
    println!("==================================================\n");

    // ---- 创建一个带生命周期的结构体实例 ----
    let 小说 = String::from("叫我以实玛利。几年前...");
    let 第一句;
    {
        // find() 查找字符位置，unwrap_or() 找不到时返回默认值
        let 句号位置 = 小说.find('。').unwrap_or(小说.len());
        第一句 = &小说[..句号位置]; // 截取从开头到句号位置
    }

    // 创建 Excerpt 实例，借用第一句
    let 摘录 = Excerpt { text: 第一句 };
    println!("摘录内容：{}", 摘录.text);
    println!("摘录长度：{} 个字符", 摘录.len());

    // ---- 结构体生命周期的实际意义 ----
    // Excerpt 的实例不能比 小说 活得更久
    // 因为 摘录.text 借用了 小说 的内容
    // 如果 小说 被释放了，摘录.text 就变成了悬垂引用

    // ---- 用方法操作带生命周期的结构体 ----
    let 文本 = String::from("Hello, Rust! 学习编程很开心。");
    let 高亮 = TextPart { text: &文本 };
    println!("文本部分：{}", 高亮.first_n_chars(5));

    // ---- 拆解结构体定义 ----
    // struct Excerpt<'a> {
    //     text: &'a str,
    // }
    //        ^^         ^^^^^^^^
    //        |              |
    //   声明生命周期参数   text 的生命周期是 'a
    //
    // 意思：Excerpt 实例借用了 text，实例的生命周期不超过 text

    println!();
}

// ---- 带生命周期的结构体 ----
// <'a> 表示这个结构体有一个生命周期参数
// text: &'a str 表示 text 字段是一个引用，生命周期是 'a
// 这意味着：Excerpt 的实例不能比它借用的 text 活得更久
struct Excerpt<'a> {
    text: &'a str,
}

// 为 Excerpt 实现方法
// 生命周期参数在 impl 后面也要声明
impl<'a> Excerpt<'a> {
    // 获取摘录内容（返回引用，生命周期由编译器自动推断）
    // 因为只有一个输入引用 &self，编译器知道返回值来自 self
    fn len(&self) -> usize {
        self.text.len()
    }
}

// 另一个带生命周期的结构体
struct TextPart<'a> {
    text: &'a str,
}

impl<'a> TextPart<'a> {
    // 截取前 n 个字符
    fn first_n_chars(&self, n: usize) -> &str {
        // char_indices() 返回每个字符的起始字节位置
        // 我们找第 n 个字符的起始位置
        let mut end = self.text.len();
        for (i, (pos, _)) in self.text.char_indices().enumerate() {
            if i == n {
                end = pos;
                break;
            }
        }
        &self.text[..end]
    }
}

// ============================================================================
// 第六部分：生命周期省略规则 —— 编译器的"自动挡"
// ============================================================================
//
// 【类比时间：自动挡 vs 手动挡】
//
// 开车有自动挡和手动挡：
//   - 手动挡：你自己换挡（手动标注生命周期）
//   - 自动挡：车子帮你换挡（编译器自动推断）
//
// Rust 编译器有 3 条"自动推断"规则：
//   如果你的代码符合这些规则，就不需要手动标注！
//   就像自动挡——你只管踩油门，换挡交给车子。
//
// 【三条省略规则】
//
// 规则 1：每个引用参数都获得自己的生命周期
//   fn foo(x: &str, y: &str)  →  fn foo<'a, 'b>(x: &'a str, y: &'b str)
//   就像每本书都有自己的借书卡。
//
// 规则 2：如果只有一个输入生命周期，它被赋给所有输出引用
//   fn foo(x: &str) -> &str  →  fn foo<'a>(x: &'a str) -> &'a str
//   就像你只借了一本书，还书期限当然和借书期限一样。
//
// 规则 3：如果有 &self 或 &mut self，self 的生命周期被赋给所有输出引用
//   fn method(&self, x: &str) -> &str  →  返回值的生命周期 = self 的生命周期
//   就像从自己口袋里拿出来的东西，当然归自己管。
//

fn 生命周期省略规则() {
    println!("==================================================");
    println!("  第五节：生命周期省略规则");
    println!("==================================================\n");

    // ---- 规则 1 演示 ----
    // 每个引用参数都有自己的生命周期
    // fn calculate_length(s: &str) -> usize
    // 等价于 fn calculate_length<'a>(s: &'a str) -> usize
    // 但返回值是 usize（不是引用），所以不需要生命周期
    let s = String::from("Hello, Rust!");
    println!("字符串长度：{}", calculate_length(&s));

    // ---- 规则 2 演示 ----
    // 只有一个输入引用，它的生命周期自动赋给输出引用
    // fn first_word(s: &str) -> &str
    // 等价于 fn first_word<'a>(s: &'a str) -> &'a str
    let 句子 = String::from("hello world 你好世界");
    println!("第一个单词：{}", first_word(&句子));

    // ---- 规则 3 演示 ----
    // 方法中 &self 的生命周期赋给输出引用
    // fn len(&self) -> usize  —— 返回值不是引用，不需要生命周期
    // fn first_n_chars(&self, n: usize) -> &str  —— 返回值的生命周期 = self
    let 文本 = String::from("Rust 很有趣");
    let 部分 = TextPart { text: &文本 };
    println!("前3个字符：{}", 部分.first_n_chars(3));

    // ---- 什么时候必须手动标注？ ----
    // 当编译器无法用这 3 条规则推断时，你必须手动标注！
    // 例如：有两个输入引用，返回一个引用
    //   fn longest(x: &str, y: &str) -> &str  ← 编译器报错！
    //   fn longest<'a>(x: &'a str, y: &'a str) -> &'a str  ← 手动标注

    // ---- 总结 ----
    println!("  生命周期省略规则总结：");
    println!("  规则1：每个引用参数都有自己的生命周期");
    println!("  规则2：只有一个输入生命周期 → 它赋给所有输出");
    println!("  规则3：有 &self → self 的生命周期赋给所有输出");
    println!("  如果这 3 条规则都不够用，就必须手动标注！\n");
}

// ---- 辅助函数：计算字符串长度 ----
// 返回值是 usize（不是引用），所以不需要生命周期标注
// 编译器用规则 1 推断：输入的 &str 有自己的生命周期
fn calculate_length(s: &str) -> usize {
    s.len()
}

// ---- 辅助函数：获取第一个单词 ----
// 只有一个输入引用，编译器用规则 2 推断：
// 输出 &str 的生命周期 = 输入 &str 的生命周期
fn first_word(s: &str) -> &str {
    let bytes = s.as_bytes();
    for (i, &byte) in bytes.iter().enumerate() {
        // 空格是单词的分隔符
        if byte == b' ' {
            return &s[..i]; // 返回从开头到空格之前的部分
        }
    }
    s // 没有空格，整个字符串就是第一个单词
}

// ============================================================================
// 第七部分：'static 生命周期 —— "永久借阅卡"
// ============================================================================
//
// 【类比时间：永久借阅卡】
//
// 有些书是"永久借阅"的：
//   - 它们被印在程序里，和程序一起"永生"
//   - 程序运行多久，它们就存在多久
//   - 永远不会过期，永远可以看
//
// 'static 就是"永久借阅卡"：
//   - 表示引用在整个程序运行期间都有效
//   - 最常见的例子：字符串字面量 "hello"
//   - 字符串字面量被直接嵌入到编译后的二进制文件中
//   - 程序启动时它们就在，程序结束时它们才消失
//
// 【为什么字符串字面量是 'static？】
//
// 当你写 let s = "hello"; 时：
//   - "hello" 这 5 个字节被写进了程序的二进制文件
//   - 程序运行时，这 5 个字节一直在内存里
//   - 所以 &s 的生命周期 = 程序的生命周期 = 'static
//
// 就像一本刻在石头上的书——只要石头在，书就在。
//

fn static生命周期() {
    println!("==================================================");
    println!("  第六节：'static 生命周期");
    println!("==================================================\n");

    // ---- 字符串字面量是 'static ----
    let s: &'static str = "我会一直存在，直到程序结束";
    println!("'static 字符串：{}", s);

    // ---- 为什么字符串字面量是 'static？ ----
    // 当你写 "hello" 时，Rust 会把这 5 个字节嵌入到二进制文件
    // 程序启动时加载到内存，程序结束时才释放
    // 所以它的生命周期 = 程序的生命周期 = 'static
    let 字面量 = "这是字面量";
    println!("字面量：{}", 字面量);

    // ---- &str 和 String 的区别 ----
    // &str 是引用（借用别人的字符串）
    // String 是拥有所有权的字符串
    let 拥有的 = String::from("我是 String，我拥有这些字符");  // 拥有所有权
    let 借用的: &str = "我是 &str，我是字面量的引用";  // 'static 生命周期
    println!("{}", 拥有的);
    println!("{}", 借用的);

    // ---- 'static 不代表"永远不会被清理" ----
    // 它只是说"这个引用在程序运行期间一直有效"
    // 真正的内存管理还是由所有权系统负责

    // ---- 什么时候会用到 'static？ ----
    // 1. 字符串字面量（最常见）
    // 2. 需要跨线程传递的数据（后面并发课程会讲）
    // 3. 全局常量

    println!();
}

// ============================================================================
// 第八部分（补充知识）：Box::leak —— 如何制造 'static
// ============================================================================
//
// 【类比时间：把书"捐献"给博物馆】
//
// 想象你有一本书（String），本来到期就会被回收：
//   - 你把它捐献给博物馆（Box::leak）
//   - 博物馆承诺永远保管这本书
//   - 从此这本书变成了"永久展品"（'static 引用）
//
// Box::leak 的作用：
//   - 把一个拥有所有权的值"泄漏"掉（永远不释放）
//   - 返回一个 'static 引用
//   - 这个值永远不会被清理（内存泄漏！）
//
// 【什么时候用 Box::leak？】
//
// 1. 你需要一个 'static 引用，但数据不是字面量
// 2. 你确实希望这个数据"活到程序结束"
// 3. 配置信息、全局状态等
//
// 【警告】
// Box::leak 会导致内存泄漏！正常使用中很少需要它。
// 这里只是让你知道有这个工具，不要滥用！
//

fn box_leak制造静态生命周期() {
    println!("==================================================");
    println!("  第七节（补充）：Box::leak 制造 'static");
    println!("==================================================\n");

    // ---- 普通的 String 不是 'static ----
    let s = String::from("我是一个普通的 String");
    // s 的类型是 String，不是 &'static str
    // 当 s 离开作用域时，内存会被释放

    // ---- 用 Box::leak 制造 'static ----
    // Box::new 创建一个堆上的值
    // Box::leak "泄漏"这个值，返回 &'static mut 引用
    let 静态字符串: &'static str = Box::leak(s.into_boxed_str());
    // 注意：s 的所有权被转移了，这里不能再用 s
    println!("'static 引用：{}", 静态字符串);

    // ---- 另一个例子 ----
    let 动态内容 = String::from("这段内容在运行时生成");
    let 静态引用: &'static str = Box::leak(动态内容.into_boxed_str());
    println!("动态内容变成 'static：{}", 静态引用);

    // ---- Box::leak 的实际用途 ----
    // 1. 配置信息：程序启动时读取配置，之后一直需要
    // 2. 全局单例：需要在多处共享的不可变数据
    // 3. 与需要 'static 的 API 交互

    // ---- 注意：Box::leak 会造成内存泄漏 ----
    // 被泄漏的内存永远不会被回收
    // 在大多数情况下，你应该优先使用正常的所有权和借用
    // 只在确实需要 'static 且没有其他办法时才用 Box::leak

    println!();
}

