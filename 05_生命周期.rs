// ---- 文本高亮器结构体 ----
// <'a> 表示 Highlight 借用了一个生命周期为 'a 的字符串
// text 字段是一个 &str 引用，不拥有字符串的所有权
struct Highlight<'a> {
    text: &'a str,
}

// 为 Highlight 实现方法
// <'a> 在 impl 后面也要声明，告诉编译器这个 impl 块用到了生命周期 'a
impl<'a> Highlight<'a> {
    // 创建一个新的高亮器
    // 接受一个 &str 引用，返回 Highlight 实例
    fn new(text: &'a str) -> Highlight<'a> {
        Highlight { text }
    }

    // 检查文本是否包含某个模式
    // &self 表示借用自身（不可变引用）
    // pattern 是另一个 &str，和 self 的生命周期无关
    fn contains(&self, pattern: &str) -> bool {
        // String 的 contains 方法检查是否包含子串
        self.text.contains(pattern)
    }

    // 获取第一个单词
    // 返回 &str，生命周期 = self.text 的生命周期（由规则 3 自动推断）
    fn first_word(&self) -> &str {
        let bytes = self.text.as_bytes();
        for (i, &byte) in bytes.iter().enumerate() {
            // 空格是单词分隔符
            if byte == b' ' {
                return &self.text[..i];
            }
        }
        // 没有空格，整个文本就是第一个单词
        self.text
    }

    // 截取前 n 个字符（注意：是字符数，不是字节数）
    // 中文一个字符 = 3 个字节，英文一个字符 = 1 个字节
    // 所以不能直接用 &self.text[..n]，需要按字符边界截取
    fn first_n_chars(&self, n: usize) -> &str {
        // char_indices() 返回每个字符的起始字节位置
        // 例如 "你好world" 的 char_indices：
        //   (0, '你'), (3, '好'), (6, 'w'), (7, 'o'), (8, 'r'), (9, 'l'), (10, 'd')
        let mut end = self.text.len(); // 默认到末尾
        for (i, (byte_pos, _)) in self.text.char_indices().enumerate() {
            if i == n {
                end = byte_pos; // 找到第 n 个字符的起始位置
                break;
            }
        }
        &self.text[..end]
    }

    // 搜索所有匹配位置
    // 返回一个 Vec<usize>，包含所有匹配的起始字节位置
    fn find_all(&self, pattern: &str) -> Vec<usize> {
        let mut 位置列表 = Vec::new();
        let mut 搜索起点 = 0;

        // 循环查找：每次从上一次找到的位置之后继续搜索
        while 搜索起点 < self.text.len() {
            // find 从指定位置开始搜索
            if let Some(位置) = self.text[搜索起点..].find(pattern) {
                let 绝对位置 = 搜索起点 + 位置;
                位置列表.push(绝对位置);
                搜索起点 = 绝对位置 + pattern.len(); // 跳过已找到的部分
            } else {
                break; // 没有更多匹配
            }
        }

        位置列表
    }

    // 获取所有匹配的文本片段
    // 返回 Vec<&str>，每个元素是一个匹配的切片
    fn get_matches(&self, pattern: &str) -> Vec<&'a str> {
        let mut 匹配列表 = Vec::new();
        let mut 搜索起点 = 0;
        let 模式长度 = pattern.len();

        while 搜索起点 < self.text.len() {
            if let Some(位置) = self.text[搜索起点..].find(pattern) {
                let 绝对位置 = 搜索起点 + 位置;
                // 截取匹配的片段
                let 片段 = &self.text[绝对位置..绝对位置 + 模式长度];
                匹配列表.push(片段);
                搜索起点 = 绝对位置 + 模式长度;
            } else {
                break;
            }
        }

        匹配列表
    }

    // 高亮打印：在终端中用 [ ] 标记匹配的部分
    fn highlight_print(&self, pattern: &str) {
        let 位置列表 = self.find_all(pattern);
        if 位置列表.is_empty() {
            println!("  （未找到 '{}'）", pattern);
            println!("  原文：{}", self.text);
            return;
        }

        print!("  高亮：");
        let mut 上一个结束 = 0;
        let 模式长度 = pattern.len();

        for &位置 in &位置列表 {
            // 打印匹配前的普通文本
            print!("{}", &self.text[上一个结束..位置]);
            // 打印高亮标记（用方括号包围）
            print!("[{}]", &self.text[位置..位置 + 模式长度]);
            上一个结束 = 位置 + 模式长度;
        }
        // 打印最后一个匹配之后的文本
        println!("{}", &self.text[上一个结束..]);
    }
}

