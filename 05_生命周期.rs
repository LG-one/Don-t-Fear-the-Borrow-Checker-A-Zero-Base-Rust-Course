// 练习 1 的参考函数实现
fn 返回较大的<'a>(a: &'a i32, b: &'a i32) -> &'a i32 {
    if a > b { a } else { b }
}

fn 获取切片(s: &str, start: usize, end: usize) -> &str {
    // 这里没有手动标注，编译器用规则 2 自动推断
    // 只有一个输入引用 s，输出 &str 的生命周期 = s 的生命周期
    &s[start..end]
}

fn 首字母大写(s: &str) -> &str {
    // 同样不需要手动标注（规则 2）
    s
}

