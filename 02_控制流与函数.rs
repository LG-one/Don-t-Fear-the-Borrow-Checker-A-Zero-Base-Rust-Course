/*
 * 核心收获：
 * - 控制流是程序的"大脑"：if 让程序做判断，loop/while/for 让程序重复劳动，
 *   match 让程序精确匹配多种情况
 * - 函数是代码的"积木块"：通过 fn 定义，接受参数、返回结果，
 *   让代码可复用、可测试、可维护
 * - "表达式 vs 语句"是 Rust 的核心设计哲学：
 *   几乎一切都是表达式，最后一行无分号即返回值，
 *   这让 Rust 代码既简洁又安全
 *
 * 常见陷阱：
 * - 函数最后一行忘记去掉分号：加了分号变成语句，返回 () 而不是你想要的值，
 *   编译器会报 "expected i32, found ()" 错误
 * - loop/while 中忘记更新循环变量或忘记写 break：导致死循环，
 *   程序会一直运行不退出，只能强制终止（Ctrl+C）
 *
 * 下节课预告：
 * - 下节课我们将学习"所有权"——Rust 最独特、最核心的特性，
 *   它让 Rust 不需要垃圾回收器就能保证内存安全
 */
