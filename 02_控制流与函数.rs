// ─── 练习 2：阶乘和幂运算（应用练习）────────────────────────────────────
//
//  【任务】
//  实现两个数学函数，分别用"循环"和"递归"两种方式：
//
//  1. 阶乘 factorial(n) = n! = 1 × 2 × 3 × ... × n
//     例：5! = 1 × 2 × 3 × 4 × 5 = 120
//
//  2. 幂运算 power(base, exp) = base 的 exp 次方
//     例：power(2, 10) = 1024
//
//  【什么是递归？】
//  函数自己调用自己。
//
//  生活类比：
//  你问"什么是递归？"，别人说"递归就是递归的定义"。
//  （开玩笑的！）
//
//  更好的类比——俄罗斯套娃：
//  - 打开大娃娃，里面有个小娃娃
//  - 打开小娃娃，里面还有更小的娃娃
//  - 直到最里面那个打不开的（基线条件/base case）
//
//  递归的两个要素：
//  1. 基线条件：什么时候停下来（不再调用自己）
//  2. 递归步骤：把问题缩小，调用自己
//
//  比如 factorial(5)：
//  factorial(5) = 5 × factorial(4)
//  factorial(4) = 4 × factorial(3)
//  factorial(3) = 3 × factorial(2)
//  factorial(2) = 2 × factorial(1)
//  factorial(1) = 1  ← 基线条件！不再递归
//
//  然后往回算：
//  factorial(1) = 1
//  factorial(2) = 2 × 1 = 2
//  factorial(3) = 3 × 2 = 6
//  factorial(4) = 4 × 6 = 24
//  factorial(5) = 5 × 24 = 120
// ────────────────────────────────────────────────────────────────────────

fn exercise_factorial() {
    println!("--- 练习 2：阶乘与幂运算 ---\n");

    // --- 阶乘：循环版 ---
    fn factorial_loop(n: u64) -> u64 {
        let mut result = 1; // 累乘器，初始值为 1（乘法的"中性元素"）
        for i in 1..=n {
            result *= i; // result = result × i
        }
        result
    }

    // --- 阶乘：递归版 ---
    fn factorial_recursive(n: u64) -> u64 {
        if n <= 1 {
            return 1; // 基线条件：0! = 1, 1! = 1
        }
        n * factorial_recursive(n - 1) // 递归步骤：n × (n-1)!
    }

    // --- 幂运算：循环版 ---
    fn power_loop(base: i64, exp: u32) -> i64 {
        let mut result = 1; // 累乘器
        for _ in 0..exp {
            result *= base; // 乘以 base，重复 exp 次
        }
        result
    }

    // --- 幂运算：递归版 ---
    fn power_recursive(base: i64, exp: u32) -> i64 {
        if exp == 0 {
            return 1; // 基线条件：任何数的 0 次方 = 1
        }
        base * power_recursive(base, exp - 1) // 递归步骤
    }

    // 测试阶乘
    println!("  === 阶乘测试 ===");
    for n in 0..=10 {
        let result_loop = factorial_loop(n);
        let result_rec = factorial_recursive(n);
        // 两种方式的结果应该一样
        assert_eq!(result_loop, result_rec); // assert! 用于测试，不相等时程序会 panic
        println!("  {}! = {}", n, result_loop);
    }

    // 测试幂运算
    println!("\n  === 幂运算测试 ===");
    let test_cases = [(2, 10), (3, 5), (5, 3), (10, 0), (7, 1)];
    for (base, exp) in test_cases {
        let result_loop = power_loop(base, exp);
        let result_rec = power_recursive(base, exp);
        assert_eq!(result_loop, result_rec);
        println!("  {}^{} = {}", base, exp, result_loop);
    }

    // 递归 vs 循环的选择：
    // - 循环：更省内存（不会因为调用层数太深而栈溢出）
    // - 递归：代码更简洁，更符合数学定义
    // - 实际开发中：简单问题用循环，复杂问题（如树、图遍历）用递归

    println!();
}


// ─── 练习 3：REPL 计算器（进阶挑战）────────────────────────────────────
//
//  【任务】
//  实现一个简单的交互式计算器：
//  - 支持四则运算：加(+)、减(-)、乘(*)、除(/)
//  - 输入格式：数字 运算符 数字（如 3 + 5）
//  - 输入 quit 退出
//
//  【什么是 REPL？】
//  REPL = Read-Eval-Print Loop（读取-求值-打印-循环）
//
//  生活类比：
//  REPL 就像一个对话式的服务窗口：
//  1. 你说一句话（Read：读取输入）
//  2. 工作人员处理你的请求（Eval：计算）
//  3. 工作人员告诉你结果（Print：打印输出）
//  4. "请问还需要什么？"（Loop：回到第 1 步）
//
//  【提示】
//  - 用 loop 循环
//  - 每次循环：读取输入 → 解析 → 计算 → 输出
//  - 输入 "quit" 时 break 退出
//  - 用 .split_whitespace() 按空格分割输入字符串
//  - 用 match 匹配运算符
// ────────────────────────────────────────────────────────────────────────

fn exercise_repl_calc() {
    println!("--- 练习 3：REPL 计算器 ---\n");
    println!("  使用方式：数字 运算符 数字（如 3 + 5）");
    println!("  支持运算：+  -  *  /");
    println!("  输入 quit 退出\n");

    loop {
        println!("  请输入表达式：");

        // 读取输入
        let mut input = String::new();
        std::io::stdin()
            .read_line(&mut input)
            .expect("读取输入失败");

        let input = input.trim(); // 去掉换行符和首尾空格

        // 检查是否退出
        if input == "quit" || input == "exit" {
            println!("  再见！");
            break;
        }

        // 解析输入：按空格分割成三个部分
        let parts: Vec<&str> = input.split_whitespace().collect();
        // split_whitespace() 按空白字符分割
        // collect() 把迭代器收集到 Vec（动态数组）中

        // 检查是否有 3 个部分
        if parts.len() != 3 {
            println!("  格式错误！请输入：数字 运算符 数字（如 3 + 5）\n");
            continue;
        }

        // 解析第一个数字
        let num1: f64 = match parts[0].parse() {
            Ok(n) => n,
            Err(_) => {
                println!("  '{}' 不是有效的数字！\n", parts[0]);
                continue;
            }
        };

        // 获取运算符
        let operator = parts[1];

        // 解析第二个数字
        let num2: f64 = match parts[2].parse() {
            Ok(n) => n,
            Err(_) => {
                println!("  '{}' 不是有效的数字！\n", parts[2]);
                continue;
            }
        };

        // 用 match 进行计算
        let result = match operator {
            "+" => Some(num1 + num2),
            "-" => Some(num1 - num2),
            "*" => Some(num1 * num2),
            "/" => {
                if num2 == 0.0 {
                    println!("  错误：不能除以零！\n");
                    None
                } else {
                    Some(num1 / num2)
                }
            }
            _ => {
                println!("  不支持的运算符 '{}'！支持：+ - * /\n", operator);
                None
            }
        };

        // 输出结果
        if let Some(value) = result {
            // if let 是 match 的简写形式，只关心一个分支
            println!("  {} {} {} = {}\n", num1, operator, num2, value);
        }
    }
}


