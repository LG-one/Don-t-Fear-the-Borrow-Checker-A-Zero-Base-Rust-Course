fn guess_number_game() {
    println!("------ Part 9: 猜数字游戏 ------\n");
    println!("  欢迎来到猜数字游戏！");
    println!("  我想了一个 1~100 的数字，你能猜到吗？\n");

    // --- 生成伪随机数 ---
    // 获取系统时间的"秒数"，用它作为种子
    // SystemTime::now() 返回当前时间
    // .duration_since(UNIX_EPOCH) 计算从 1970-01-01 到现在的秒数
    let secret_number = generate_random_number(1, 100);
    // 注意：由于 secret_number 在调试时会被打印出来，
    // 真正的游戏版本应该注释掉下面这行
    println!("  [调试提示] 正确答案是：{}", secret_number);

    let mut attempts = 0; // 记录猜测次数

    // 用 loop 实现"反复猜测直到猜对"
    loop {
        // --- 读取用户输入 ---
        println!("  请输入你的猜测（1~100）：");

        let mut input = String::new(); // 创建一个空的字符串来存储输入

        // std::io::stdin().read_line() 从键盘读取一行输入
        // &mut input 表示"把读到的内容写入 input 变量"
        // .expect() 处理可能的错误（读取失败时程序崩溃并提示）
        std::io::stdin()
            .read_line(&mut input)
            .expect("读取输入失败！");

        // --- 将字符串转换为数字 ---
        // 用户输入的是字符串（如 "42\n"），我们需要转成数字
        // .trim() 去掉末尾的换行符 \n
        // .parse() 尝试把字符串解析为数字
        // .parse() 返回 Result 类型，用 match 处理成功和失败两种情况
        let guess: i32 = match input.trim().parse() {
            Ok(num) => num,      // 解析成功，得到数字
            Err(_) => {
                // 解析失败（用户输入了非数字内容）
                println!("  请输入有效的数字！\n");
                continue; // 跳过本次循环，重新要求输入
            }
        };

        attempts += 1; // 每次猜测，次数加 1

        // --- 判断猜测结果 ---
        if guess < secret_number {
            println!("  {} 太小了！再大一点。\n", guess);
        } else if guess > secret_number {
            println!("  {} 太大了！再小一点。\n", guess);
        } else {
            // 猜对了！
            println!("  恭喜你猜对了！答案就是 {}！", secret_number);
            println!("  你总共猜了 {} 次。", attempts);

            // 根据次数给出评价
            let evaluation = match attempts {
                1 => "天才！一次就中！",
                2..=5 => "非常厉害！",
                6..=10 => "还不错！",
                _ => "继续加油！",
            };
            println!("  评价：{}\n", evaluation);

            break; // 猜对了，跳出循环，游戏结束
        }
    }
}

// 生成伪随机数的函数
// 原理：用当前时间的"纳秒"部分做种子，通过简单的数学运算生成"看起来随机"的数
fn generate_random_number(min: i32, max: i32) -> i32 {
    // 获取系统时间
    let now = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .expect("获取时间失败");

    // 取纳秒部分作为种子
    // as 表示类型转换：u128 → i32
    let seed = now.subsec_nanos() as i32;

    // 简单的伪随机算法：
    // 1. 取绝对值（避免负数）
    // 2. 对范围取余
    // 3. 加上最小值，确保在 [min, max] 范围内
    let range = max - min + 1; // 范围大小
    let random = (seed.abs() % range) + min;

    // 确保结果在范围内（取余可能得到负数的边界情况）
    if random < min {
        min
    } else if random > max {
        max
    } else {
        random
    }
}


