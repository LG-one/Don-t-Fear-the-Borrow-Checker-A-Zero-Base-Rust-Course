// ============================================================================
//  Part 1：if / else if / else 条件判断
// ============================================================================
//
//  【生活类比】
//  想象你早上出门前看天气：
//  - 如果下雨 → 带伞
//  - 如果不下雨但很热 → 带墨镜
//  - 否则 → 啥也不带
//
//  这就是条件判断：根据不同的"条件"，执行不同的"动作"。
//  在程序里，我们用 if / else if / else 来实现这种逻辑。
//
//  【语法规则】
//  if 条件 {
//      条件为 true 时执行的代码
//  } else if 另一个条件 {
//      前面不满足，这个满足时执行
//  } else {
//      以上都不满足时执行
//  }
//
//  注意：Rust 的 if 不需要小括号！条件表达式本身必须是 bool 类型。
// ============================================================================

fn demo_if_else() {
    println!("------ Part 1: if / else if / else ------\n");

    // --- 基本用法 ---
    let temperature = 35; // 模拟室外温度：35 度

    // Rust 的 if 不需要小括号包裹条件
    // 但条件必须是一个"布尔表达式"（结果为 true 或 false）
    if temperature > 30 {
        println!("温度 {} 度，好热！开空调！", temperature);
    } else if temperature > 20 {
        println!("温度 {} 度，很舒适！", temperature);
    } else if temperature > 10 {
        println!("温度 {} 度，有点凉，加件外套吧。", temperature);
    } else {
        println!("温度 {} 度，太冷了！裹紧棉袄！", temperature);
    }

    // --- if 是表达式！可以把结果赋值给变量 ---
    // 这是 Rust 和很多语言（如 C、Java）不同的地方：
    // 在 Rust 里，if 不仅仅是一个"控制流工具"，它还是一个"表达式"，
    // 意味着它会"返回"一个值。
    //
    // 生活类比：你问朋友"今天吃什么？"，朋友的回答（火锅/面条/随便）
    // 就是一个"值"，你可以把这个回答记下来。

    let weather = "下雨";
    let action = if weather == "下雨" {
        "带伞"       // 注意：这里没有分号！这就是返回值
    } else if weather == "大太阳" {
        "带墨镜"     // 分支类型必须一致
    } else {
        "空手出门"   // 所有分支返回相同类型
    };
    // 类比：action 就像一张"行动指令卡"，根据天气写上了不同的指令
    println!("天气：{} → 行动：{}", weather, action);

    // --- 常见错误演示 ---
    // 错误写法（在其他语言中可能合法，但在 Rust 中不行）：
    // let number = if true { 5 } else { "hello" };
    // 原因：两个分支返回不同类型（一个是整数，一个是字符串）
    // Rust 要求：所有分支必须返回相同的类型

    // --- 嵌套 if（不推荐太深嵌套，3 层以上考虑用 match 或函数拆分）---
    let age = 25;
    let has_id = true;

    if age >= 18 {
        if has_id {
            println!("年龄 {}，有身份证 → 允许进入。", age);
        } else {
            println!("年龄 {}，但没带身份证 → 不能进入。", age);
        }
    } else {
        println!("年龄 {}，未成年 → 禁止进入。", age);
    }

    // --- 用 if 做简单的数值判断 ---
    let score = 85;
    let grade = if score >= 90 {
        "优秀"
    } else if score >= 80 {
        "良好"
    } else if score >= 60 {
        "及格"
    } else {
        "不及格"
    };
    println!("成绩 {} 分 → 等级：{}\n", score, grade);
}


// ============================================================================
//  Part 2：loop 无限循环与 break
// ============================================================================
//
//  【生活类比】
//  想象一台洗衣机：
//  - 它会不停地转（循环）
//  - 直到衣服洗干净了，它才停下来（break）
//
//  loop 就是"一直做，直到我叫你停"。
//  如果没有 break，程序会永远运行下去——这叫"死循环"。
//
//  【语法规则】
//  loop {
//      // 循环体：这里写的代码会反复执行
//      if 停止条件 {
//          break;  // 跳出循环
//      }
//  }
// ============================================================================

fn demo_loop_basic() {
    println!("------ Part 2: loop 基础 ------\n");

    // --- 最简单的 loop：计数 ---
    // 类比：你数数，从 1 数到 5 就停
    let mut count = 0; // mut 表示"可变的"，因为 count 会不断变化

    loop {
        count += 1; // 每次循环，count 加 1
        println!("  数到第 {} 个", count);

        if count >= 5 {
            println!("  数到 5 了，停！\n");
            break; // break = 跳出循环，就像按下了洗衣机的"停止"按钮
        }
    }

    // --- 用 loop 实现"做某事直到成功"的模式 ---
    // 类比：你投篮，直到投进为止
    let mut attempts = 0;
    let target = 3; // 假设第 3 次投进

    loop {
        attempts += 1;
        if attempts == target {
            println!("  第 {} 次投篮，进了！总共投了 {} 次。\n", attempts, attempts);
            break;
        } else {
            println!("  第 {} 次投篮，没进，继续...", attempts);
        }
    }
}

fn demo_loop_break_value() {
    println!("------ Part 2 扩展: loop + break 返回值 ------\n");

    // 【高级特性】break 可以"带出"一个值！
    // 类比：你在一个迷宫里找钥匙（循环），找到了之后，
    //       不仅要"走出迷宫"（break），还要"把钥匙带出来"（返回值）。

    let mut counter = 0;

    // 整个 loop 表达式会返回 break 后面的值
    let result = loop {
        counter += 1;
        if counter == 10 {
            break counter * 2; // break 后面跟一个值，这个值就是 loop 的返回值
            // break 20 → 所以 result = 20
        }
    };
    // 类比：result 就是你从迷宫里带出来的钥匙
    println!("  counter 到达 {}，loop 返回值：{}", counter, result);

    // --- break + continue ---
    // continue = 跳过本次循环的剩余部分，直接进入下一次
    // 类比：你在翻一摞文件，遇到"已处理"的文件就跳过（continue），
    //       遇到"机密"文件就停下来（break）。

    println!("\n  演示 continue：打印 1~10 中的奇数");
    let mut n = 0;
    loop {
        n += 1;
        if n > 10 {
            break; // 超过 10 就停
        }
        if n % 2 == 0 {
            continue; // 偶数跳过，不执行下面的 println
        }
        println!("  奇数：{}", n);
    }
    println!();
}


// ============================================================================
//  Part 3：while 循环
// ============================================================================
//
//  【生活类比】
//  loop 像是"一直做，直到我说停"——停止条件藏在循环体里面。
//  while 像是"只要条件还成立，就继续做"——停止条件写在门口。
//
//  比喻：
//  - loop：你闭着眼睛跑步，直到撞到墙才停。（先跑，再判断）
//  - while：你睁着眼睛跑步，看到墙就停。（先判断，再跑）
//
//  【语法规则】
//  while 条件 {
//      条件为 true 时反复执行
//  }
//  // 条件变为 false 时，循环自动结束，不需要 break
// ============================================================================

fn demo_while() {
    println!("------ Part 3: while 循环 ------\n");

    // --- 基本 while ---
    // 类比：你有一叠饼要烙，只要还有没烙的饼，就继续烙
    let mut pancakes_to_cook = 5;

    while pancakes_to_cook > 0 {
        println!("  烙第 {} 张饼...", 6 - pancakes_to_cook); // 6 - 5 = 第1张
        pancakes_to_cook -= 1; // 烙完一张，剩余数量减 1
    }
    println!("  所有饼都烙完了！\n");

    // --- while 遍历数组（不推荐，有更好的方式）---
    let fruits = ["苹果", "香蕉", "橘子", "葡萄", "西瓜"];
    let mut index = 0;

    println!("  水果清单（用 while 遍历）：");
    while index < fruits.len() {
        // .len() 返回数组的长度
        println!("    {}. {}", index + 1, fruits[index]);
        index += 1;
    }

    // 注意：上面这种用 index 遍历的方式虽然能用，但不够"优雅"。
    // 在 Rust 中，我们更推荐用 for 循环（见 Part 4）。
    // while 遍历容易犯的错：忘记写 index += 1，导致死循环！
    println!();
}


// ============================================================================
//  Part 4：for 循环与范围
// ============================================================================
//
//  【生活类比】
//  while 像是"只要还有客人，就继续招待"——你不知道会有多少客人。
//  for 像是"招待座位上这 10 位客人"——你知道确切的数量。
//
//  for 循环是 Rust 中最常用的循环，因为它：
//  - 不容易写出死循环（不需要手动管理计数器）
//  - 语法简洁清晰
//  - 性能优秀（编译器能更好地优化）
//
//  【范围语法】
//  1..10   → 从 1 到 9（不包含 10），左闭右开 [1, 10)
//  1..=10  → 从 1 到 10（包含 10），左闭右闭 [1, 10]
// ============================================================================

fn demo_for() {
    println!("------ Part 4: for 循环与范围 ------\n");

    // --- 基本 for 循环：遍历范围 ---
    // 类比：老师点名，从 1 号点到 5 号
    println!("  点名（1..6 表示 1 到 5）：");
    for i in 1..6 {
        // 1..6 表示 1, 2, 3, 4, 5（不包含 6）
        // 类比：从 1 号房间走到 5 号房间，6 号房间不进去
        println!("    {} 号同学到！", i);
    }

    println!("\n  点名（1..=5 表示 1 到 5，包含 5）：");
    for i in 1..=5 {
        // 1..=5 表示 1, 2, 3, 4, 5（包含 5）
        // 和上面效果一样，但含义更明确："包含终点"
        println!("    {} 号同学到！", i);
    }

    // --- 为什么有两种写法？ ---
    // 1..10  → 不包含右端点，适合数组索引（因为索引从 0 开始）
    // 1..=10 → 包含右端点，适合"数数"场景（第 1 个到第 10 个）

    // --- for 遍历数组 ---
    // 这是 Rust 中最推荐的遍历方式！
    let cities = ["北京", "上海", "广州", "深圳", "杭州"];

    println!("\n  城市列表（for 遍历数组）：");
    for city in cities.iter() {
        // .iter() 创建一个迭代器，每次循环取出一个元素
        // 类比：你手里有一串糖葫芦，从第一个开始，一个一个吃
        println!("    - {}", city);
    }

    // --- 带索引的遍历：enumerate ---
    println!("\n  城市列表（带编号）：");
    for (index, city) in cities.iter().enumerate() {
        // enumerate() 同时给你"编号"和"内容"
        // 类比：你拿着一本花名册，一边报序号一边报名字
        println!("    {}. {}", index + 1, city);
    }

    // --- 反向遍历：rev ---
    println!("\n  倒计时：");
    for i in (1..=5).rev() {
        // .rev() = reverse，反转顺序
        // 类比：火箭发射倒计时：5, 4, 3, 2, 1
        println!("    {}...", i);
    }
    println!("  发射！\n");

    // --- 步长遍历（用 step_by）---
    println!("  偶数（2 到 20，步长 2）：");
    for i in (2..=20).step_by(2) {
        // step_by(2) = 每次跳 2 步
        // 类比：你上楼梯，每次跨 2 级台阶
        print!("  {} ", i);
    }
    println!("\n");

    // --- for 循环中使用 _ 忽略变量 ---
    // 当你只需要"重复 N 次"而不关心当前是第几次时
    println!("  重复 3 次的问候：");
    for _ in 0..3 {
        // _ 是"占位符"，表示"我不需要这个值"
        // 类比：你只需要"拍 3 下手"，不需要数是第几下
        println!("    你好！");
    }
    println!();
}


// ============================================================================
//  Part 5：match 表达式（初探）
// ============================================================================
//
//  【生活类比】
//  if/else 像是"是非题"——只有对或错。
//  match 像是"多选题"——把一个值和多个选项逐一比较，找到匹配的那个。
//
//  更形象的比喻：
//  你去自动贩卖机买饮料：
//  - 投了 1 元 → 出矿泉水
//  - 投了 3 元 → 出可乐
//  - 投了 5 元 → 出果汁
//  - 其他金额 → 退币
//
//  match 就是这台贩卖机：输入一个值，匹配到对应分支，返回对应结果。
//
//  【注意】
//  这里只做简单介绍，match 的完整用法会在"枚举"那一课详细讲解。
// ============================================================================

fn demo_match() {
    println!("------ Part 5: match 表达式（初探）------\n");

    // --- 基本 match ---
    let number = 3;

    // match 的工作方式：
    // 1. 拿到 number 的值（3）
    // 2. 从上到下逐个匹配
    // 3. 找到第一个匹配的分支，执行对应代码
    // 4. 匹配结束后，自动跳出（不需要 break！）
    match number {
        1 => println!("  一"),     // number 是 1 时执行
        2 => println!("  二"),     // number 是 2 时执行
        3 => println!("  三"),     // number 是 3 时执行 ← 匹配到这里！
        4 => println!("  四"),     // 不会执行，因为已经在上面匹配到了
        _ => println!("  其他"),   // _ 是"通配符"，匹配所有剩余情况
    }

    // --- match 也是表达式，可以返回值 ---
    // 类比：贩卖机投币后"吐出"饮料
    let coin = 3;
    let drink = match coin {
        1 => "矿泉水",
        3 => "可乐",
        5 => "果汁",
        _ => "金额不足，退币",  // 必须覆盖所有可能！（Rust 的要求）
    };
    println!("\n  投入 {} 元 → 获得：{}", coin, drink);

    // --- match 匹配多个值 ---
    let day = "周六";
    let day_type = match day {
        "周一" | "周二" | "周三" | "周四" | "周五" => "工作日",
        // | 表示"或"，匹配多个值中的任意一个
        "周六" | "周日" => "周末",
        _ => "无效的日期",
    };
    println!("  {} 是{}", day, day_type);

    // --- match 匹配范围 ---
    let score = 85;
    let grade = match score {
        90..=100 => "优秀",    // 90 到 100（包含两端）
        80..=89  => "良好",
        60..=79  => "及格",
        0..=59   => "不及格",
        _        => "分数无效",
    };
    println!("  {} 分 → {}\n", score, grade);

    // 注意：match 的所有分支必须穷尽所有可能！
    // 如果漏掉了某种情况，编译器会报错。
    // 用 _ 通配符来覆盖"其他所有情况"。
}


// ============================================================================
//  Part 6：函数定义 fn、参数、返回值
// ============================================================================
//
//  【生活类比】
//  函数就像一台"机器"：
//  - 参数 = 你放进机器的原料
//  - 函数体 = 机器的工作过程
//  - 返回值 = 机器生产出来的产品
//
//  比如一台"榨汁机"：
//  - 输入：苹果（参数）
//  - 过程：清洗、榨汁、过滤（函数体）
//  - 输出：苹果汁（返回值）
//
//  【语法规则】
//  fn 函数名(参数名: 参数类型) -> 返回类型 {
//      // 函数体
//      // 最后一行没有分号 = 返回值
//  }
//
//  关键点：
//  - Rust 要求你必须明确写出参数类型和返回类型（不能省略）
//  - 这叫"静态类型"——编译器在编译时就知道所有类型
//  - 好处：减少运行时错误，性能更好
// ============================================================================

fn demo_functions() {
    println!("------ Part 6: 函数 ------\n");

    // --- 调用无参数无返回值的函数 ---
    greet();

    // --- 调用有参数的函数 ---
    greet_person("G-one");

    // --- 调用有返回值的函数 ---
    let sum = add(10, 20);
    println!("  10 + 20 = {}", sum);

    // --- 调用有多个参数的函数 ---
    let area = rectangle_area(5, 3);
    println!("  长 5 宽 3 的矩形面积 = {}", area);

    // --- 调用带返回值的函数，并直接用于判断 ---
    let num = 42;
    if is_even(num) {
        println!("  {} 是偶数", num);
    } else {
        println!("  {} 是奇数", num);
    }

    // --- 函数作为"工具"组合使用 ---
    let temperature_f = 98.6;
    let temperature_c = fahrenheit_to_celsius(temperature_f);
    println!("  {}°F = {:.1}°C", temperature_f, temperature_c);
    println!();
}

// 函数定义：无参数，无返回值
// 类比：一台只会打招呼的机器，不需要输入，也不输出什么
fn greet() {
    println!("  大家好！欢迎来到 Rust 课堂！");
}

// 函数定义：有一个参数，无返回值
// 参数格式：参数名: 参数类型
// 类比：一台只会叫名字的机器，需要你塞一张名片（参数）进去
fn greet_person(name: &str) {
    // &str 是"字符串切片"类型，后面会详细讲
    println!("  你好，{}！很高兴认识你！", name);
}

// 函数定义：有两个参数，有返回值
// 返回值类型写在 -> 后面
// 类比：一台加法计算器，输入两个数字，输出它们的和
fn add(a: i32, b: i32) -> i32 {
    // a + b   ← 这是最后一行，没有分号！
    // 在 Rust 中，函数体最后一行没有分号 = 这就是返回值
    // 就像机器的最后一道工序是"把产品放上传送带"
    a + b
    // 注意：不写 return，也不写分号，这就是"表达式返回"
}

// 多参数函数
fn rectangle_area(length: i32, width: i32) -> i32 {
    length * width
}

// 返回布尔值的函数
fn is_even(n: i32) -> bool {
    n % 2 == 0
    // n % 2 求余数，余数为 0 就是偶数
    // 整个表达式的结果是 true 或 false
}

// 温度转换：华氏度 → 摄氏度
fn fahrenheit_to_celsius(f: f64) -> f64 {
    (f - 32.0) * 5.0 / 9.0
}


// ============================================================================
//  Part 7：表达式 vs 语句
// ============================================================================
//
//  这是 Rust 中一个非常重要的概念，也是很多初学者困惑的地方。
//
//  【生活类比】
//  想象你在做菜：
//
//  语句（Statement）= 做一个动作，但不产生结果
//  例："切菜" → 你切完了，但手里没有"一个值"
//  在代码中：let x = 5; 这就是语句，它"声明了一个变量"，但不返回值
//
//  表达式（Expression）= 做一个计算，产生一个结果
//  例："3 + 5" → 计算结果是 8，你手里有一个"值"
//  在代码中：3 + 5 就是表达式，它的结果是 8
//
//  【核心规则】
//  1. 几乎所有 Rust 代码都是表达式
//  2. 加了分号 ; 就变成了语句（"丢弃"返回值）
//  3. 不加分号 = 把这个值作为返回值
//
//  这就是为什么函数最后一行不加分号就能返回值！
// ============================================================================

fn demo_expression_vs_stmt() {
    println!("------ Part 7: 表达式 vs 语句 ------\n");

    // --- 表达式 vs 语句的对比 ---

    // 代码块 {} 也是表达式！它的值是最后一个表达式的值
    let y = {
        let x = 3;    // 这是语句（有分号）
        x + 1         // 这是表达式（没有分号）← 这个值就是整个块的值
        // 类比：整个 {} 是一个"黑盒子"，进去的是原料，出来的是 x + 1 = 4
    };
    // 所以 y = 4
    println!("  y = {} （代码块最后一行无分号，返回 x + 1 = 4）", y);

    // 对比：如果加了分号
    let z = {
        let x = 3;
        x + 1;        // 加了分号！这变成了语句，返回值被"丢弃"了
        // 这个代码块的最后一行变成了"空"，返回 ()
        // () 在 Rust 中叫"单元类型"，类似其他语言的 void
    };
    // z 的类型是 ()，不是 i32！
    println!("  z = {:?} （代码块最后一行有分号，返回 () 单元类型）", z);

    // --- if 也是表达式 ---
    let condition = true;
    let number = if condition { 5 } else { 6 };
    //              ↑ 表达式 1        ↑ 表达式 2
    // 整个 if 的结果赋值给 number
    println!("  number = {} （if 表达式的结果）", number);

    // --- 函数调用也是表达式 ---
    let result = add(7, 8);
    println!("  add(7, 8) = {} （函数调用表达式）", result);

    // --- 常见错误：在表达式后面加了分号 ---
    // fn wrong() -> i32 {
    //     5;    // 加了分号 → 变成语句 → 返回 () → 类型不匹配 → 编译错误！
    // }
    // 错误信息：expected `i32`, found `()`
    // 修复：去掉分号即可

    println!("\n  记住：函数最后一行想要返回值，就不要加分号！");
    println!("  加了分号 = 语句（丢弃值）；不加分号 = 表达式（保留值）\n");
}


// ============================================================================
//  Part 8：提前 return
// ============================================================================
//
//  【生活类比】
//  你在排队买奶茶：
//  - 正常情况：排到你，点单，付款，拿奶茶，走人（函数正常执行到最后）
//  - 提前离开：你发现钱包丢了，立刻离开队伍（提前 return）
//
//  有些时候，函数不需要执行完所有代码就能给出结果。
//  比如：检查密码是否正确，发现不正确就立刻返回错误，不需要继续检查。
//
//  【语法规则】
//  return 表达式;   ← 提前返回，函数立刻结束
//  注意：return 后面要加分号！（这和"最后一行无分号"不同）
// ============================================================================

fn demo_early_return() {
    println!("------ Part 8: 提前 return ------\n");

    // --- 使用提前 return 做"守卫检查" ---
    let numbers = [10, 20, 30, 40, 50];
    let target = 30;

    let position = find_in_array(&numbers, target);
    match position {
        Some(index) => println!("  在数组中找到 {}，位置索引：{}", target, index),
        None => println!("  在数组中没找到 {}", target),
    }

    // --- 提前 return 的常见用途：参数验证 ---
    let result = divide(10.0, 3.0);
    println!("  10 / 3 = {:.2}", result);

    let result = divide(10.0, 0.0);
    println!("  10 / 0 = {:.2} （特殊值表示除零错误）", result);

    println!();
}

// 在数组中查找目标值，返回其索引（用 Option 类型表示"可能找不到"）
// &[] 表示"借用"数组，不需要复制整个数组（所有权的概念后面会讲）
fn find_in_array(arr: &[i32], target: i32) -> Option<usize> {
    // Option<usize> 表示"可能有值（Some），也可能没有（None）"
    // 类比：你在一个盒子里找钥匙，找到了（Some(钥匙)）或者没找到（None）

    for (index, &item) in arr.iter().enumerate() {
        if item == target {
            return Some(index); // 找到了！立刻返回，不再继续找
            // return 会立刻结束函数，把 Some(index) 交出去
        }
    }

    None // 循环结束都没找到，返回 None
    // 注意：这是最后一行，没有 return 关键字，也没有分号
    // 两种写法都可以：return None; 或者 None
}

// 除法函数，提前检查除数是否为零
fn divide(a: f64, b: f64) -> f64 {
    if b == 0.0 {
        println!("  错误：除数不能为零！");
        return 0.0; // 提前返回，不执行后面的除法
        // 类比：你发现传送带上有坏苹果，立刻拿走，不进入后续加工
    }

    // 只有除数不为零时才会执行到这里
    a / b
}


